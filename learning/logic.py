# Logical Operations

# Identity operator
a = 100
b = a

print (a is b) # ref comparison

# Comparison operators

print (a < b or b > a or a == b or a != b)

a = 5

print (0 <= a <= 10) # chaining

# Membership operator
c = [3, 6, 9]

print (6 in c)

# Logic operators

print (not 7 and 5 or 3) # ! && ||